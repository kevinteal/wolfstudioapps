<div id="about" class="text_single faded_bg"><hr class="style-one">The Bet Penguin uses machine learning techniques and principals to predict the result of football matches. It uses data gathered from the last few seasons to calculate the result.  <hr class="style-one"></div>


<div id="features" class="feature_box"><div class="feature_left"><div class="feature_img bet_feature_bg"><img src="images/breakdown.png" width="348" height="300" /> </div></div><div class="feature_right">
The predictions are calculated by comparing the two teams <span class="blue_text">fixture history</span>. Each teams last 6 games is evaluated to figure out <span class="blue_text">form</span>. The league positions are also considered, as well as the fixtures each team has played against teams that are <span class="blue_text">similar to the opposition</span>. 

</div></div>

<div class="feature_box"><div class="feature_right">
Similar teams to the opposition are teams that <span class="blue_text">are/were in the same area of the league table</span> when they played a fixture against a team in a similar place in the table to the original team. Data from these matches are <span class="blue_text">weighted</span> to a certain percent in the <span class="blue_text">prediction outcome</span>.
</div><div class="feature_left"><div class="feature_img bet_feature_bg"><img src="images/related.png" width="273" height="300" /></div></div></div>

<div class="feature_box"><div class="feature_left"><div class="feature_img bet_feature_bg"><img src="images/think.png" width="132" height="263" /></div></div><div class="feature_right">
The prediction uses  <span class="blue_text">machine learning</span> in the format that, before each prediction is finalised, it will check its own  <span class="blue_text">accuracy</span> in previous predictions of similar matches. If the predictions have been excessively wrong, the system will <span class="blue_text">adjust the prediction accordingly</span>. 
</div></div>


